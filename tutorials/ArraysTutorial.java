package tutorials;
public class ArraysTutorial {
    public static void main(String[] args) {
        // Create an array of integers.
        int[] numbers = new int[5];
        // Set the value of the first element to 1.
        numbers[0] = 1;
        // Set the value of the second element to 2.
        numbers[1] = 2;
        numbers[2] = 3;
        numbers[3] = 4;
        numbers[4] = 5;
        // Print the value of the first element.
        System.out.println(numbers[0]);

        int[] nums = {1, 2, 3, 4, 5};
        System.out.println(nums[0]);
    }
}
